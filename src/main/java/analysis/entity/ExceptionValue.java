package analysis.entity;

public class ExceptionValue
{
    private double value;
    private int exceptionStat;

    public double getValue()
    {
        return value;
    }

    public void setValue(double value)
    {
        this.value = value;
    }

    public int getExceptionStat()
    {
        return exceptionStat;
    }

    public void setExceptionStat(int exceptionStat)
    {
        this.exceptionStat = exceptionStat;
    }
}
